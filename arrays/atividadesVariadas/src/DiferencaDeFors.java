public class DiferencaDeFors {
    public static void main(String[] args) throws Exception {
        int[] pares = {2, 4, 6, 8, 10};
        for(int i = 0; i < pares.length; i++){
            System.out.println(pares[i]);
        }
        System.out.println(); 
        for (int num : pares) {
          System.out.println(num);  
        } 
    }
}
